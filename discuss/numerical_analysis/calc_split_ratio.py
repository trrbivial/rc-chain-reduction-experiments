data = [[14811, 1380], [17036, 1371], [30030, 1870], [34401, 987], [7548, 181],
        [14697, 1619], [56598, 6757], [46788, 2260], [37603, 0], [11414, 772]]

sum = 0
for i in range(len(data)):
    n, m = data[i]
    sum += m / n
    print(f"{m / n * 100:.2f}%")

sum /= len(data)
print(f"{sum * 100:.2f}%")
